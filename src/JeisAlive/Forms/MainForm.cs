    // ── Helpers ─────────────────────────────────────────────────

    private void AppendLog(string msg)
    {
        if (InvokeRequired)
        {
            Invoke(() => AppendLog(msg));
            return;
        }

        txtLog.AppendText(msg + "\r\n");
    }

    private void SetProgress(int value)
    {
        if (InvokeRequired)
        {
            Invoke(() => SetProgress(value));
            return;
        }

        prgProgress.Value = Math.Clamp(value, 0, 100);
    }
